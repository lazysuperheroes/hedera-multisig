#!/usr/bin/env node
/**
 * Walkthrough step 2 (dApp): create on-chain Hedera accounts for the
 * signing keys generated in step 1.
 *
 * Why this step exists
 * --------------------
 * HashPack and other WalletConnect-compatible Hedera wallets sign via
 * an *account*, not a raw key. The wallet's import-private-key flow
 * derives the public key, queries the mirror node for an account whose
 * key matches, and binds the WalletConnect session to that account ID.
 * If no on-chain account has the public key, the wallet either refuses
 * to import or imports a "key-only" entry that can't sign because
 * there's no `accountId` for the SDK to put on the transaction.
 *
 * The keys generated by `01-generate-keys.js` are pure crypto material
 * — no associated Hedera accounts. To enable the hosted-dApp signing
 * path, we create one Hedera account per generated key, with that key
 * as the account's controlling key. That gives HashPack something to
 * bind to when alice imports her private key — the wallet's mirror
 * lookup finds the account this script just created.
 *
 * Cost: ~0.55 ℏ per account (small AccountCreate fee + 0.5 ℏ initial
 * balance), so ~1.65 ℏ total paid by the operator. The signer-account
 * balances are unused — they're not the multi-sig treasury, they're
 * not paying ceremony fees, they exist solely as WalletConnect
 * identity. The 0.5 ℏ initial balance just makes the wallet's mirror
 * lookup more reliable.
 *
 * Idempotent: re-running the script when `walkthrough-signer-accounts.json`
 * already exists is a no-op (won't burn extra HBAR creating duplicates).
 * Delete the file to force re-creation.
 */

const fs = require('fs');
const path = require('path');
require('dotenv').config({ path: path.resolve(__dirname, '..', '..', '.env') });

const {
  Client, AccountId, PrivateKey, PublicKey,
  AccountCreateTransaction, Hbar,
} = require('@hashgraph/sdk');
const chalk = require('chalk');

const KEYS_FILE = path.join(__dirname, 'walkthrough-keys.json');
const SIGNER_ACCOUNTS_FILE = path.join(__dirname, 'walkthrough-signer-accounts.json');
const PER_SIGNER_BALANCE_HBAR = 0.5;

async function main() {
  console.log(chalk.bold.cyan('\n━━━ Create on-chain Hedera accounts for signing keys ━━━\n'));

  if (!fs.existsSync(KEYS_FILE)) {
    fail(`${KEYS_FILE} not found. Run: node 01-generate-keys.js`);
  }

  const keysData = JSON.parse(fs.readFileSync(KEYS_FILE, 'utf8'));
  const signers = Object.entries(keysData.keys);
  if (signers.length !== 2) {
    fail(`Expected 2 keys in walkthrough-keys.json (alice + agent), found ${signers.length}`);
  }

  for (const k of ['OPERATOR_ID', 'OPERATOR_KEY']) {
    if (!process.env[k]) fail(`Missing env var: ${k}.`);
  }

  // Idempotent skip — if all three accounts are already created and
  // recorded, don't burn another 1.65 ℏ on duplicates.
  if (fs.existsSync(SIGNER_ACCOUNTS_FILE)) {
    try {
      const existing = JSON.parse(fs.readFileSync(SIGNER_ACCOUNTS_FILE, 'utf8'));
      const allCreated = signers.every(([name]) => existing.accounts?.[name]?.accountId);
      if (allCreated) {
        console.log(chalk.yellow('⚠  Signer accounts already exist — skipping creation:'));
        for (const [name, acc] of Object.entries(existing.accounts)) {
          console.log(chalk.gray(`     ${name.padEnd(8)} ${acc.accountId}`));
        }
        console.log(chalk.gray('\n   Delete walkthrough-signer-accounts.json to re-create.\n'));
        console.log(chalk.bold.cyan('Next: node 03-create-threshold-account.js\n'));
        process.exit(0);
      }
    } catch {
      // Corrupt file — proceed with creation.
    }
  }

  const network = (process.env.HEDERA_NETWORK || 'testnet').toLowerCase();
  const client = network === 'mainnet' ? Client.forMainnet() : Client.forTestnet();
  client.setOperator(
    AccountId.fromString(process.env.OPERATOR_ID),
    PrivateKey.fromString(process.env.OPERATOR_KEY)
  );

  const accounts = {};
  for (const [name, { publicKey }] of signers) {
    const pub = PublicKey.fromString(publicKey);
    console.log(chalk.gray(`Creating account for ${name} (key: ${publicKey.slice(0, 24)}…)...`));
    const tx = new AccountCreateTransaction()
      .setKey(pub)
      .setInitialBalance(new Hbar(PER_SIGNER_BALANCE_HBAR))
      .setAccountMemo(`hedera-multisig walkthrough (dApp) — signer account for ${name}`);
    const submit = await tx.execute(client);
    const receipt = await submit.getReceipt(client);
    const accountId = receipt.accountId.toString();
    accounts[name] = { accountId, publicKey };
    console.log(chalk.green(`  ✅ ${name.padEnd(8)} ${accountId}`));
  }

  const out = {
    network,
    note: 'Per-signer Hedera accounts. Each has the matching signing key from walkthrough-keys.json as its account-key. These accounts exist solely so HashPack and other WalletConnect-compatible wallets can bind a session to them — they do NOT pay fees for the multi-sig ceremony. The 2-of-3 threshold account (created by 03-create-threshold-account.js) is the multi-sig treasury.',
    perSignerBalanceHbar: PER_SIGNER_BALANCE_HBAR,
    createdAt: new Date().toISOString(),
    accounts,
  };
  fs.writeFileSync(SIGNER_ACCOUNTS_FILE, JSON.stringify(out, null, 2));

  console.log(chalk.bold.green('\n✅ Signer accounts created on testnet:'));
  for (const [name, { accountId }] of Object.entries(accounts)) {
    console.log(`   ${name.padEnd(8)} ${accountId}  https://hashscan.io/${network}/account/${accountId}`);
  }
  console.log(chalk.gray(`\n   State saved: ${SIGNER_ACCOUNTS_FILE}`));
  console.log(chalk.gray('   alice → import into HashPack (or any WalletConnect wallet) for'));
  console.log(chalk.gray('   the human signing path. agent → keep on this machine; the long-'));
  console.log(chalk.gray('   running 04-run-agent.js loads the encrypted file directly.'));

  console.log(chalk.bold.cyan('\nNext: node 03-create-threshold-account.js\n'));

  client.close();
}

function fail(msg) {
  console.error(chalk.red('\n❌ ') + msg + '\n');
  process.exit(1);
}

main().catch((err) => fail(err.stack || err.message));
