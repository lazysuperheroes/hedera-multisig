#!/usr/bin/env node
/**
 * Walkthrough step 1 (dApp): generate three Ed25519 signing keys.
 *
 * Identical to `examples/walkthrough-hbar/01-generate-keys.js` — duplicated
 * here so this walkthrough is fully self-contained. Reusing keys from
 * the HBAR walkthrough? Copy them across instead of regenerating:
 *
 *   cp ../walkthrough-hbar/walkthrough-keys.* .
 *
 * Writes (per signer):
 *   walkthrough-keys.{alice,bob,carol}.encrypted  — AES-256-GCM encrypted
 *
 * Plus an internal plaintext index used by step 2:
 *   walkthrough-keys.json  — { keys: { alice, bob, carol } }
 *
 * Encryption passphrase: WALKTHROUGH_PASSPHRASE constant below. Documented
 * for the walkthrough, ≥12 chars to satisfy `EncryptedFileProvider`.
 * **Testnet only.** A real production deployment generates each key on a
 * separate device by a separate person, with a strong passphrase per signer.
 *
 * For the public-dApp signing flow you'll also import each signer's key
 * into a Hedera-compatible wallet (HashPack is the reference; any
 * WalletConnect-compatible wallet works). See the README for that step.
 */

const fs = require('fs');
const path = require('path');
const { PrivateKey } = require('@hashgraph/sdk');
const chalk = require('chalk');
const EncryptedFileProvider = require('../../keyManagement/EncryptedFileProvider');

const SIGNERS = ['alice', 'bob', 'carol'];
const OUT_DIR = __dirname;

const WALKTHROUGH_PASSPHRASE = 'walkthrough-test';

function main() {
  console.log(chalk.bold.cyan('\n━━━ Generate signing keys (dApp walkthrough) ━━━\n'));

  const keys = {};
  for (const name of SIGNERS) {
    const priv = PrivateKey.generateED25519();
    keys[name] = {
      privateKey: priv.toString(),
      publicKey: priv.publicKey.toString(),
    };

    const encryptedPath = path.join(OUT_DIR, `walkthrough-keys.${name}.encrypted`);
    EncryptedFileProvider.createEncryptedFile(
      [priv.toString()],
      WALKTHROUGH_PASSPHRASE,
      encryptedPath,
      { description: `walkthrough-token testnet key (${name})` }
    );

    console.log(chalk.green('✓'), chalk.bold(name.padEnd(8)), priv.publicKey.toString().slice(0, 24) + '…');
  }

  const combinedPath = path.join(OUT_DIR, 'walkthrough-keys.json');
  fs.writeFileSync(combinedPath, JSON.stringify({
    note: 'TESTNET ONLY — generated by examples/walkthrough-token/01-generate-keys.js. Do not commit. Do not reuse on mainnet.',
    generatedAt: new Date().toISOString(),
    passphrase: WALKTHROUGH_PASSPHRASE,
    keys,
  }, null, 2));

  console.log(chalk.bold.green('\n✅ Keys written:'));
  console.log(chalk.gray('   Encrypted (one per signer — for wallet import + reference):'));
  for (const name of SIGNERS) {
    console.log(chalk.gray(`     ${path.join(OUT_DIR, `walkthrough-keys.${name}.encrypted`)}`));
  }
  console.log(chalk.gray('   Plaintext index (used by step 2 + for wallet imports):'));
  console.log(chalk.gray(`     ${combinedPath}`));

  console.log(chalk.bold.white(`\n   Passphrase: ${chalk.cyan(WALKTHROUGH_PASSPHRASE)}`));
  console.log(chalk.gray('   (Documented constant — testnet walkthrough only)'));

  console.log(chalk.yellow('\n⚠  Testnet keys. Delete after the walkthrough — they are gitignored but'));
  console.log(chalk.yellow('   should never be committed or reused on mainnet.'));

  console.log(chalk.bold.cyan('\nNext: node 02-create-signer-accounts.js\n'));
}

main();
