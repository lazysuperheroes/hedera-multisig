#!/usr/bin/env node
/**
 * Walkthrough step 1 (ECDSA): generate three secp256k1 signing keys.
 *
 * Differs from walkthrough-dapp's step 1 in exactly one line:
 * `PrivateKey.generateECDSA()` instead of `PrivateKey.generateED25519()`.
 * Everything downstream — account creation, threshold KeyList, signature
 * collection, verification, executor, mirror confirmation — is key-type-
 * agnostic and was already exercised by the mixed-type tests, but this
 * walkthrough exists to confirm an end-to-end ECDSA-only ceremony works
 * the same as the Ed25519 default.
 *
 * Why ECDSA matters on Hedera
 * ---------------------------
 * Hedera natively supports both Ed25519 and secp256k1 (ECDSA) keys. ECDSA
 * is what most non-Hedera-native users already have via Ethereum-compatible
 * wallets. While MetaMask / Rabby cannot directly sign Hedera SDK
 * transactions today (they speak the EIP-155 WalletConnect namespace, not
 * the Hedera one), HashPack / Blade / Kabila all import ECDSA private keys
 * the same way they import Ed25519 ones. This walkthrough validates that
 * the entire stack — coordinator, dApp, CLI participant, mirror — handles
 * ECDSA cleanly in a real ceremony.
 *
 * Writes (per signer):
 *   walkthrough-keys.{alice,bob,carol}.encrypted  — AES-256-GCM encrypted
 *
 * Plus an internal plaintext index for HashPack imports + step 2:
 *   walkthrough-keys.json  — { keys: { alice, bob, carol } }
 *
 * Encryption passphrase: WALKTHROUGH_PASSPHRASE constant below.
 * **Testnet only.** Real production keys are generated per-device,
 * per-person, with strong per-signer passphrases.
 */

const fs = require('fs');
const path = require('path');
const { PrivateKey } = require('@hashgraph/sdk');
const chalk = require('chalk');
const EncryptedFileProvider = require('../../keyManagement/EncryptedFileProvider');

const SIGNERS = ['alice', 'bob', 'carol'];
const OUT_DIR = __dirname;

const WALKTHROUGH_PASSPHRASE = 'walkthrough-test';

// secp256k1 (ECDSA) DER-encoded SubjectPublicKeyInfo prefix. If a generated
// pubkey starts with this, it's confirmed ECDSA — useful as a visible
// sanity check that we got what we asked for.
const ECDSA_PUBKEY_DER_PREFIX = '302d300706052b8104000a032200';

function main() {
  console.log(chalk.bold.cyan('\n━━━ Generate signing keys (ECDSA walkthrough) ━━━\n'));

  const keys = {};
  for (const name of SIGNERS) {
    const priv = PrivateKey.generateECDSA();
    const pubHex = priv.publicKey.toString();

    // Cryptographic sanity check — this is the *point* of this walkthrough.
    // If the SDK ever changed `generateECDSA` to silently fall back to
    // Ed25519 on platforms where secp256k1 isn't available, the validation
    // would fail here, not silently pass through to a Hedera ceremony where
    // mixed-type signature verification would mask the issue.
    if (!pubHex.toLowerCase().startsWith(ECDSA_PUBKEY_DER_PREFIX)) {
      console.error(chalk.bold.red(
        `\n❌ Generated key for ${name} doesn't have the secp256k1 DER prefix.\n` +
        `   Expected prefix: ${ECDSA_PUBKEY_DER_PREFIX}\n` +
        `   Got pubkey:      ${pubHex.slice(0, 32)}…\n` +
        `   This is a bug in @hashgraph/sdk's PrivateKey.generateECDSA(),\n` +
        `   or in this script's prefix expectation.\n`
      ));
      process.exit(1);
    }

    keys[name] = {
      privateKey: priv.toString(),
      publicKey: pubHex,
      keyType: 'ECDSA_SECP256K1',
      // EVM address derived from the secp256k1 public key — the same address
      // MetaMask would show for this key. Not used by our ceremony (Hedera
      // identifies signers by their full public key in a KeyList), but useful
      // for cross-referencing if you ever import this key into an EVM tool.
      evmAddress: '0x' + priv.publicKey.toEvmAddress(),
    };

    const encryptedPath = path.join(OUT_DIR, `walkthrough-keys.${name}.encrypted`);
    EncryptedFileProvider.createEncryptedFile(
      [priv.toString()],
      WALKTHROUGH_PASSPHRASE,
      encryptedPath,
      { description: `walkthrough-ecdsa testnet key (${name}) — secp256k1` }
    );

    console.log(chalk.green('✓'), chalk.bold(name.padEnd(8)),
      pubHex.slice(0, 28) + '…',
      chalk.gray('(EVM:'), chalk.gray(keys[name].evmAddress + ')')
    );
  }

  const combinedPath = path.join(OUT_DIR, 'walkthrough-keys.json');
  fs.writeFileSync(combinedPath, JSON.stringify({
    note: 'TESTNET ONLY — generated by examples/walkthrough-ecdsa/01-generate-keys.js. ECDSA secp256k1 keys. Do not commit. Do not reuse on mainnet.',
    keyType: 'ECDSA_SECP256K1',
    generatedAt: new Date().toISOString(),
    passphrase: WALKTHROUGH_PASSPHRASE,
    keys,
  }, null, 2));

  console.log(chalk.bold.green('\n✅ ECDSA keys written:'));
  console.log(chalk.gray('   Encrypted (one per signer — for wallet import + reference):'));
  for (const name of SIGNERS) {
    console.log(chalk.gray(`     ${path.join(OUT_DIR, `walkthrough-keys.${name}.encrypted`)}`));
  }
  console.log(chalk.gray('   Plaintext index:'));
  console.log(chalk.gray(`     ${combinedPath}`));

  console.log(chalk.bold.white(`\n   Passphrase: ${chalk.cyan(WALKTHROUGH_PASSPHRASE)}`));
  console.log(chalk.gray('   (Documented constant — testnet walkthrough only)'));

  console.log(chalk.bold.cyan('\n   All three keys validated as secp256k1 (ECDSA).'));
  console.log(chalk.gray('   Sanity check: each public key DER-encodes with the secp256k1 prefix'));
  console.log(chalk.gray(`   ${ECDSA_PUBKEY_DER_PREFIX} (vs Ed25519's 302a300506032b6570…).`));

  console.log(chalk.yellow('\n⚠  Testnet keys. Delete after the walkthrough — they are gitignored but'));
  console.log(chalk.yellow('   should never be committed or reused on mainnet.'));

  console.log(chalk.bold.cyan('\nNext: node 02-create-signer-accounts.js\n'));
}

main();
