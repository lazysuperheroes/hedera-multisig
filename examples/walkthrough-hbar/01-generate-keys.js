#!/usr/bin/env node
/**
 * Walkthrough step 1: generate three Ed25519 signing keys.
 *
 * Writes:
 *   walkthrough-keys.json              — combined file with all three keys
 *   walkthrough-keys.alice.json        — single-signer file (CLI participant)
 *   walkthrough-keys.bob.json          — single-signer file
 *   walkthrough-keys.carol.json        — single-signer file
 *
 * NOTE: Keys are written in plaintext for testnet walkthrough simplicity.
 * For production multi-sig, generate each key on a separate device, never
 * write them all in one place, and always use encrypted-file format
 * (see EncryptedFileProvider — `keyManagement/EncryptedFileProvider.js`).
 */

const fs = require('fs');
const path = require('path');
const { PrivateKey } = require('@hashgraph/sdk');
const chalk = require('chalk');

const SIGNERS = ['alice', 'bob', 'carol'];
const OUT_DIR = __dirname;

function main() {
  console.log(chalk.bold.cyan('\n━━━ Generate signing keys ━━━\n'));

  const keys = {};
  for (const name of SIGNERS) {
    const priv = PrivateKey.generateED25519();
    keys[name] = {
      privateKey: priv.toString(),
      publicKey: priv.publicKey.toString(),
    };

    // Per-signer file (single-key array, simplest format the encrypted-file
    // provider also accepts as a degenerate case)
    const perSignerPath = path.join(OUT_DIR, `walkthrough-keys.${name}.json`);
    fs.writeFileSync(perSignerPath, JSON.stringify([{
      label: name,
      privateKey: priv.toString(),
      publicKey: priv.publicKey.toString(),
    }], null, 2));

    console.log(chalk.green('✓'), chalk.bold(name.padEnd(8)), priv.publicKey.toString().slice(0, 24) + '…');
  }

  // Combined file used by step 02
  const combinedPath = path.join(OUT_DIR, 'walkthrough-keys.json');
  fs.writeFileSync(combinedPath, JSON.stringify({
    note: 'TESTNET ONLY — generated by examples/walkthrough-hbar/01-generate-keys.js',
    generatedAt: new Date().toISOString(),
    keys,
  }, null, 2));

  console.log(chalk.bold.green('\n✅ Keys written:'));
  console.log(`   ${combinedPath}`);
  for (const name of SIGNERS) {
    console.log(`   ${path.join(OUT_DIR, `walkthrough-keys.${name}.json`)}`);
  }
  console.log(chalk.yellow('\n⚠  These are TESTNET keys in plaintext. Delete after the walkthrough.'));
  console.log(chalk.bold.cyan('\nNext: node 02-create-threshold-account.js\n'));
}

main();
